CREATE TABLE IF NOT EXISTS "experiment" (
	"ID" UUID NOT NULL DEFAULT gen_random_uuid(),
	"name" VARCHAR NOT NULL,
	"description" TEXT NOT NULL DEFAULT '',
	"start" TIMESTAMPTZ NOT NULL,
	"end" TIMESTAMPTZ NULL,
	"userID" UUID NOT NULL,
	"livingLabID" UUID NULL,
	CONSTRAINT "FK__user" FOREIGN KEY ("userID") REFERENCES "user" ("ID") ON UPDATE RESTRICT ON DELETE RESTRICT,
	CONSTRAINT "FK__livingLab" FOREIGN KEY ("livingLabID") REFERENCES "livingLab" ("ID") ON UPDATE RESTRICT ON DELETE RESTRICT
);